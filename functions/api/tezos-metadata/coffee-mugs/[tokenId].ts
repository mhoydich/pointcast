/**
 * /api/tezos-metadata/coffee-mugs/[tokenId] — TZIP-21 metadata for the
 * Coffee Mugs FA2 contract (KT1JQ3AjzFvMnjZ9mGqrM13aj8LQBx9JpoXt).
 *
 * Contract concatenates metadata_base_uri + "/" + tokenId + ".json"
 * (see contracts/v2/coffee_mugs_fa2.py register_tokens). The on-chain
 * metadata_base_uri was set via set_metadata_base_uri() to
 *   https://pointcast.xyz/api/tezos-metadata/coffee-mugs
 * so this endpoint answers requests like:
 *   https://pointcast.xyz/api/tezos-metadata/coffee-mugs/0.json
 *
 * Renders TZIP-12/-21 metadata that wallets (Kukai), explorers (TzKT,
 * better-call.dev), and marketplaces (objkt, teia) consume. Image URIs
 * point to static pixel-art SVGs at /images/coffee-mugs/<slug>.svg —
 * generated by scripts/generate-coffee-mug-svgs.mjs from the same data
 * that drives the /coffee page.
 */

