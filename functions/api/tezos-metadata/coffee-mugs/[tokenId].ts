const ARTIST = 'tz2FjJhB1gb9Xc2qNB7QgFkdBZkGCCRMxdFw'; // Mike's mainwallet
const CONTRACT_KT1 = 'KT1JQ3AjzFvMnjZ9mGqrM13aj8LQBx9JpoXt';

export const onRequestGet: PagesFunction<Env> = async ({ params }) => {
  const raw = (params.tokenId as string) ?? '';
  // Strip optional .json — the on-chain URI ends in .json but the route
  // captures the literal segment.
  const m = raw.match(/^(\d+)(?:\.json)?$/);
  if (!m) {
    return new Response(JSON.stringify({ error: 'invalid tokenId' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    });
  }
  const tokenId = Number(m[1]);
  const mug = TOKEN_ID_TO_MUG[tokenId];
  if (!mug) {
    return new Response(
      JSON.stringify({ error: 'tokenId out of range', valid: '0..4' }),
      { status: 404, headers: { 'Content-Type': 'application/json' } },
    );
  }

  const imageUri = `https://pointcast.xyz/images/coffee-mugs/${mug.slug}.svg`;

  const body = {
    name: `PointCast ${mug.name} #${tokenId}`,
    symbol: 'PCMUG',
    decimals: 0,
    isBooleanAmount: false,
    description: mug.description,
    displayUri: imageUri,
    thumbnailUri: imageUri,
    artifactUri: imageUri,
    mime: 'image/svg+xml',
    creators: [ARTIST],
    contributors: [ARTIST],
    publishers: ['PointCast'],
    date: '2026-04-25T20:33:00Z', // origination timestamp
    language: 'en',
    rights: 'No License / All Rights Reserved',
    rightsUri: 'https://pointcast.xyz/about',
    attributes: [
      { name: 'rarity', value: mug.rarity },
      { name: 'edition', value: String(mug.edition) },
      { name: 'threshold', value: `${mug.threshold} cup${mug.threshold === 1 ? '' : 's'}` },
      { name: 'collection', value: 'Coffee Mugs' },
    ],
    tags: ['pointcast', 'coffee-mugs', 'tezos', 'fa2', 'pixel-art', mug.rarity, mug.slug],
    royalties: {
      decimals: 4,
      shares: { [ARTIST]: 750 }, // 7.5%
    },
    formats: [
      {
        uri: imageUri,
        mimeType: 'image/svg+xml',
        dimensions: { value: '512x512', unit: 'px' },
      },
    ],
    homepage: 'https://pointcast.xyz/coffee',
    externalUri: `https://objkt.com/asset/${CONTRACT_KT1}/${tokenId}`,
  };

  return new Response(JSON.stringify(body, null, 2), {
    status: 200,
    headers: {
      'Content-Type': 'application/json; charset=utf-8',
      // Cache for 1 hour at the edge — metadata changes are rare.
      'Cache-Control': 'public, max-age=3600, s-maxage=3600',
      'Access-Control-Allow-Origin': '*',
    },
  });
};
